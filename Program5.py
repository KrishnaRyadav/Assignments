
#digit ranging from 0 to 6 and print week days starts from monday

num = int(input("Enter a week day :"))

if(num==0):
    print("Monday")
elif(num==1):
    print("Tuesday")
elif(num==2):
    print("Wednesday")
elif(num==3):
    print("Thursday")
elif(num==4):
    print("Friday")
elif(num==5):
    print("Saturday")
elif(num==6):
    print("Sunday")
else:
    print("You Entered Invalid WeekDay..!!")

